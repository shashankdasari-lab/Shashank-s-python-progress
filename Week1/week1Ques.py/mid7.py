'''7. Set Relationship Analyzer ⭐⭐
Problem Statement

Create two sets.

Example:

A = {1,2,3,4,5}
B = {4,5,6,7,8}
Display
Union
Intersection
Difference
Symmetric Difference'''

set1={1,2,3,4,5,6}
set2={4,5,6,7,8,9}

print(set1)
print(set2)
print(set1.intersection(set2))
print(set1.union(set2))
print(set1.difference(set2))
print(set1.symmetric_difference(set2))

